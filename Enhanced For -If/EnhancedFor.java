public class EnhancedFor {
    public static void main(String[] args) {
        int[] numbers = {1, 2, 3, 4, 5};
        for (int number : numbers) {
            System.out.println(number);
        }
    }
}


// Kita mendeklarasikan sebuah array bernama numbers yang berisi nilai-nilai bilangan bulat {1, 2, 3, 4, 5}.
// Loop for yang ditingkatkan menyederhanakan proses pengulangan array.
// Setiap iterasi, loop memberikan nilai elemen saat ini dari array ke variabel number, dan kita dapat mengakses dan mengoperasikan elemen ini di dalam loop.
// Loop mengulangi setiap elemen dalam array secara berurutan, mencetak setiap elemen ke konsol.